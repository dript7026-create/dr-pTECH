"""
Simple PNG/PPM -> GBA 4bpp tile exporter (prototype)

Outputs:
- <input>_tiles.bin : raw 8x8 tiles (one byte per pixel, indices 0..15)
- <input>_palette.txt : palette colors (R,G,B 0-31) one per line

Requires Pillow (`pip install pillow`).
"""
import sys
from PIL import Image

def quantize_image(img, colors=16):
    return img.convert('P', palette=Image.ADAPTIVE, colors=colors)

def extract_palette(img):
    palette = img.getpalette()[:48]  # 16 * 3
    cols = []
    for i in range(0, len(palette), 3):
        r = palette[i] >> 3
        g = palette[i+1] >> 3
        b = palette[i+2] >> 3
        cols.append((r,g,b))
    return cols

def write_tiles(img, out_tiles):
    w,h = img.size
    pixels = img.load()
    with open(out_tiles, 'wb') as f:
        for ty in range(0, h, 8):
            for tx in range(0, w, 8):
                for y in range(8):
                    for x in range(8):
                        px = pixels[tx+x, ty+y]
                        f.write(bytes([px & 0xF]))

def main():
    if len(sys.argv) < 2:
        print('Usage: convert_tiles.py input.png')
        return
    inp = sys.argv[1]
    img = Image.open(inp).convert('RGBA')
    q = quantize_image(img, 16)
    pal = extract_palette(q)
    out_tiles = inp + '_tiles.bin'
    out_pal = inp + '_palette.txt'
    write_tiles(q, out_tiles)
    with open(out_pal, 'w') as f:
        for r,g,b in pal:
            f.write(f"{r} {g} {b}\n")
    print('Wrote', out_tiles, 'and', out_pal)

if __name__ == '__main__':
    main()
